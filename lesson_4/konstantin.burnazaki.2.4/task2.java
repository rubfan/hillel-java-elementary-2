public class task2 {

    public static void main(String[] args) {
        printFiftyfiveElements();

    }

    public static void printFiftyfiveElements() {
        int loopCount = 0;
        for (int i = 1; loopCount != 56; i += 2) {
            loopCount += 1;
            System.out.println(i);
        }

    }
}
